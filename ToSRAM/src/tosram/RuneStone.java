package tosram;

/**
 * A representation of rune stone as specified in Tower of Savior.
 * 
 * @author johnchen902
 */

public enum RuneStone {

	/**
	 * The red rune stone, which has a fire sign on it.
	 */
	FIRE,

	/**
	 * The green rune stone, which has four signs of leaf on it.
	 */
	EARTH,

	/**
	 * The blue rune stone, which is shaped as a drop of water.
	 */
	WATER,

	/**
	 * The yellow rune stone, which has a cross sign on it.
	 */
	LIGHT,

	/**
	 * The purple rune stone, which has a sign of moon on it.
	 */
	DARK,

	/**
	 * The pink rune stone, which has a sign of heart on it.
	 */
	HEART,

	/**
	 * The rune stone with unknown type, which likely cannot be dissolved.
	 */
	UNKNOWN;
}
